     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validar($request->all())->validate();
        Prestamotipo::create([
            'id'=>date('Yidisus'),
            'nombre'=>$request->nombre
        ]);

        return redirect('prestamotipos')->with('mensaje', 'Tipo registrado');
    }

